/* ============================================================================
   ARENA
   Owns the four static barrier bodies and the Start Zone geometry. Exposes
   only what other modules need (spawn queries, drawing) -- nothing outside
   this class reaches into barrier body internals directly.
   ============================================================================ */
class Arena {
  #barrierBodies = [];
  #startZone;

  constructor() {
    this.#startZone = {
      x: ARENA_CONFIG.x + START_ZONE_CONFIG.marginX,
      y: ARENA_CONFIG.y + START_ZONE_CONFIG.marginY,
      w: START_ZONE_CONFIG.width,
      h: ARENA_CONFIG.height - START_ZONE_CONFIG.marginY * 2
    };
  }

  build(world, profile) {
    const opts = { isStatic: true };
    const { x, y, width, height, wallThickness } = ARENA_CONFIG;

    const top = Bodies.rectangle(x + width / 2, y - wallThickness / 2, width + wallThickness * 2, wallThickness, opts);
    const bottom = Bodies.rectangle(x + width / 2, y + height + wallThickness / 2, width + wallThickness * 2, wallThickness, opts);
    const left = Bodies.rectangle(x - wallThickness / 2, y + height / 2, wallThickness, height + wallThickness * 2, opts);
    const right = Bodies.rectangle(x + width + wallThickness / 2, y + height / 2, wallThickness, height + wallThickness * 2, opts);

    this.#barrierBodies = [top, bottom, left, right];
    for (const b of this.#barrierBodies) {
      b.isBarrier = true;
      // matter.js's Body.setStatic (triggered internally by isStatic: true
      // during Body.create) unconditionally hardcodes restitution=0 and
      // friction=1 on static bodies, discarding whatever was passed in the
      // constructor options -- restitution/friction MUST be reassigned here,
      // after construction, or the profile's barrier values are silently
      // ignored entirely (this was true even before per-mode profiles
      // existed: BARRIER_PHYSICS.restitution was never actually reaching
      // the body).
      b.restitution = profile.barrierRestitution;
      b.friction = BARRIER_FRICTION;
    }
    World.add(world, this.#barrierBodies);
  }

  teardown(world) {
    if (this.#barrierBodies.length) World.remove(world, this.#barrierBodies);
    this.#barrierBodies = [];
  }

  isInsideStartZone(x, y) {
    const z = this.#startZone;
    return x >= z.x && x <= z.x + z.w && y >= z.y && y <= z.y + z.h;
  }

  isInsideArena(x, y) {
    return x >= ARENA_CONFIG.x && x <= ARENA_CONFIG.x + ARENA_CONFIG.width &&
      y >= ARENA_CONFIG.y && y <= ARENA_CONFIG.y + ARENA_CONFIG.height;
  }

  getStartZone() {
    return { ...this.#startZone };
  }

  draw(spawnRejectTimer) {
    this.#drawFloor();
    for (const b of this.#barrierBodies) this.#drawBarrierSegment(b);
    this.#drawStartZone(spawnRejectTimer);
  }

  #drawFloor() {
    const { x, y, width, height } = ARENA_CONFIG;

    noStroke();
    fill(36, 40, 46);
    rect(x, y, width, height);

    // Faint radial sheen -- suggests polished metal under overhead light.
    const cx = x + width / 2;
    const cy = y + height / 2;
    const maxR = Math.max(width, height) * 0.7;
    const steps = 6;
    noFill();
    for (let i = steps; i > 0; i--) {
      const t = i / steps;
      stroke(255, 255, 255, (1 - t) * 10);
      strokeWeight(maxR / steps);
      circle(cx, cy, maxR * t);
    }

    // Subtle floor grid.
    stroke(255, 255, 255, 12);
    strokeWeight(1);
    for (let gx = x; gx <= x + width; gx += 40) line(gx, y, gx, y + height);
    for (let gy = y; gy <= y + height; gy += 40) line(x, gy, x + width, gy);

    // Soft ambient shading toward the edges (vignette).
    noFill();
    for (let i = 0; i < 10; i++) {
      const inset = i * 4;
      stroke(0, 0, 0, 6);
      strokeWeight(4);
      rect(x + inset, y + inset, width - inset * 2, height - inset * 2, 4);
    }
  }

  #drawBarrierSegment(body) {
    const w = body.bounds.max.x - body.bounds.min.x;
    const h = body.bounds.max.y - body.bounds.min.y;
    const horizontal = w >= h;

    push();
    translate(body.position.x, body.position.y);
    rectMode(CENTER);

    // Inner shadow cast onto the floor, drawn slightly before the rail itself.
    noStroke();
    fill(0, 0, 0, 50);
    rect(0, horizontal ? (body.position.y < ARENA_CONFIG.y + ARENA_CONFIG.height / 2 ? 6 : -6) : 0,
      horizontal ? w : w + 10,
      horizontal ? h + 10 : h, 4);

    // Outer rail (structural steel look).
    noStroke();
    fill(70, 74, 82);
    rect(0, 0, w, h);

    // Diagonal caution stripes, clipped to the segment.
    const stripeSize = 22;
    drawingContext.save();
    drawingContext.beginPath();
    drawingContext.rect(-w / 2, -h / 2, w, h);
    drawingContext.clip();
    fill(230, 180, 40);
    noStroke();
    const span = horizontal ? w : h;
    for (let i = -span; i < span; i += stripeSize * 2) {
      if (horizontal) {
        quad(i, -h / 2, i + stripeSize, -h / 2, i + stripeSize - h, h / 2, i - h, h / 2);
      } else {
        quad(-w / 2, i, -w / 2, i + stripeSize, w / 2, i + stripeSize - w, w / 2, i - w);
      }
    }
    drawingContext.restore();

    // Inner rubber padding strip facing the arena interior.
    const padThickness = 6;
    fill(30, 30, 34);
    if (horizontal) {
      const dir = body.position.y < ARENA_CONFIG.y + ARENA_CONFIG.height / 2 ? 1 : -1;
      rect(0, dir * (h / 2 - padThickness / 2), w - 4, padThickness, 3);
    } else {
      const dir = body.position.x < ARENA_CONFIG.x + ARENA_CONFIG.width / 2 ? 1 : -1;
      rect(dir * (w / 2 - padThickness / 2), 0, padThickness, h - 4, 3);
    }

    // Inner highlight edge for a layered look.
    noFill();
    stroke(255, 255, 255, 40);
    strokeWeight(2);
    rect(0, 0, w - 6, h - 6);
    pop();
  }

  #drawStartZone(spawnRejectTimer) {
    const z = this.#startZone;

    // Floor tint.
    noStroke();
    fill(255, 230, 90, 14);
    rect(z.x, z.y, z.w, z.h);

    // Hatched diagonal pattern for a "parking bay" feel.
    push();
    drawingContext.save();
    drawingContext.beginPath();
    drawingContext.rect(z.x, z.y, z.w, z.h);
    drawingContext.clip();
    stroke(255, 230, 90, 25);
    strokeWeight(1);
    for (let i = -z.h; i < z.w; i += 16) {
      line(z.x + i, z.y, z.x + i + z.h, z.y + z.h);
    }
    drawingContext.restore();
    pop();

    // Dashed outline.
    push();
    noFill();
    stroke(255, 230, 90);
    strokeWeight(2);
    drawingContext.setLineDash([8, 6]);
    rect(z.x, z.y, z.w, z.h, 6);
    drawingContext.setLineDash([]);
    pop();

    noStroke();
    fill(255, 230, 90);
    textAlign(CENTER);
    textSize(14);
    text("START ZONE", z.x + z.w / 2, z.y + 18);

    if (spawnRejectTimer > 0) {
      noFill();
      stroke(255, 60, 60, spawnRejectTimer * 12);
      strokeWeight(4);
      rect(z.x, z.y, z.w, z.h, 6);

      noStroke();
      fill(255, 70, 70, spawnRejectTimer * 12);
      textAlign(CENTER);
      textSize(13);
      text("SPAWN REJECTED - too close to another car", z.x + z.w / 2, z.y + z.h / 2);
    }
  }
}

// ===== js/extensions/DamageModel.js =====
/* ============================================================================
   DAMAGE MODEL (Extension 1 -- vertex-level persistent deformation)

   Composed by Car, one instance per body. This does not sit on top of
   matter.js's convenience API: it edits the body's actual collision polygon
   at runtime via Body.setVertices(), so a dent is not a decal -- subsequent
   collisions genuinely resolve against the damaged hull.

   Two parallel vertex sets model "squash-and-stretch now, permanent dent
   later" with a single mechanism:
     - #liveVerts  : what's currently rendered AND what the physics body uses.
                     Springs quickly toward #permVerts (short-term deformation).
     - #permVerts  : the lasting damage. Springs extremely slowly toward the
                     pristine #restVerts (so light taps eventually buff out,
                     heavy ones don't within a play session).
   ============================================================================ */
class DamageModel {
  #body;
  #profile;
  #restVerts = [];  // pristine local-frame offsets from centroid, heading-independent
  #restRadii = [];
  #liveVerts = [];
  #permVerts = [];
  #damage = 0;
  #hitFlashFrames = 0; // brief "just took a hit" signal, read by HUD to flash the damage bar

  constructor(body, headingAtBuild, profile) {
    this.#body = body;
    this.#profile = profile;

    // Capture the pristine shape in a LOCAL frame: subtract the body's
    // current position, then rotate by -heading, so the shape is described
    // independent of the body's orientation at any later point in time.
    const cx = body.position.x;
    const cy = body.position.y;
    const cos = Math.cos(-headingAtBuild);
    const sin = Math.sin(-headingAtBuild);

    for (const v of body.vertices) {
      const dx = v.x - cx;
      const dy = v.y - cy;
      const lx = dx * cos - dy * sin;
      const ly = dx * sin + dy * cos;
      this.#restVerts.push({ x: lx, y: ly });
      this.#restRadii.push(Math.hypot(lx, ly) || 1);
    }
    this.#liveVerts = this.#restVerts.map(v => ({ ...v }));
    this.#permVerts = this.#restVerts.map(v => ({ ...v }));
  }

  get damage() { return this.#damage; }

  // True for a handful of frames right after a non-trivial impact -- purely
  // a UI signal (HUD flashes the damage bar on it), computed from the same
  // registerImpact call that already knows whether a hit actually landed.
  get recentImpact() { return this.#hitFlashFrames > 0; }

  // Already local-frame offsets, so Car can hand these straight to
  // beginShape()/vertex() inside its own rotated drawing context.
  get localVertices() { return this.#liveVerts; }

  // worldContactPoint / impactMagnitude come from CollisionManager, and are
  // now derived from APPROACH VELOCITY (closing speed along the collision
  // normal, sampled at collisionStart before the solver responds) rather
  // than post-solve impulse. That decoupling matters: with restitution as
  // high as Demolition's, most of that impulse gets RETURNED to the bodies
  // as bounce instead of absorbed, so an impulse-driven damage model would
  // have made the bounciest mode the least damaging -- exactly backwards.
  // Approach velocity doesn't care what the solver does afterward, so a
  // 0.9-restitution hit and a 0.4-restitution hit at the same closing speed
  // now do comparable damage; only the bounce differs.
  registerImpact(worldContactPoint, impactMagnitude) {
    if (!isFinite(impactMagnitude)) return; // rests/grazes must never accumulate damage

    const angle = this.#body.angle;
    const cos = Math.cos(-angle);
    const sin = Math.sin(-angle);
    const dx = worldContactPoint.x - this.#body.position.x;
    const dy = worldContactPoint.y - this.#body.position.y;
    const localContact = { x: dx * cos - dy * sin, y: dx * sin + dy * cos };

    const pushStrength = constrain(impactMagnitude * this.#profile.damageScale, 0, 14);
    if (pushStrength < 0.4) return; // ignore negligible taps

    for (let i = 0; i < this.#restVerts.length; i++) {
      this.#deformVertex(i, localContact, pushStrength);
    }
    this.#recomputeDamageScalar();
    this.#hitFlashFrames = 12;
  }

  // Gaussian falloff kernel centred on the local contact point: vertices
  // near the impact deform the most, neighbours deform progressively less.
  #deformVertex(index, localContact, pushStrength) {
    const rest = this.#restVerts[index];
    const restRadius = this.#restRadii[index];
    const distToContact = Math.hypot(rest.x - localContact.x, rest.y - localContact.y);
    const sigma = Math.max(restRadius * DAMAGE_CONFIG.falloffSigmaFactor, 4);
    const weight = Math.exp(-(distToContact * distToContact) / (2 * sigma * sigma));
    if (weight < 0.02) return;

    // Push purely along this vertex's own radial (outward) direction,
    // negated -- i.e. straight toward the centroid. This is what guarantees
    // vertices can only move along a fixed angular ray from the centre, so
    // their angular ORDER around the polygon never changes: the outline
    // stays a simple (non-self-intersecting) polygon and can't invert or
    // break the SAT collision solver, regardless of how it's dented.
    const inward = { x: -rest.x / restRadius, y: -rest.y / restRadius };
    const maxPush = restRadius * DAMAGE_CONFIG.maxPushFraction;
    const basePush = Math.min(pushStrength * weight, maxPush);
    const minRadius = restRadius * DAMAGE_CONFIG.minRadiusFraction;

    // dentPushMultiplier only exaggerates the LIVE (transient/visual) pop --
    // the permanent set below is still derived from the un-multiplied
    // basePush, so a bouncier profile doesn't also mean faster wrecking;
    // damageScale (applied to pushStrength above) is what governs that.
    const livePush = basePush * this.#profile.dentPushMultiplier;
    this.#liveVerts[index] = this.#pushRadially(this.#liveVerts[index], inward, livePush, minRadius);

    const permPush = basePush * DAMAGE_CONFIG.permanentSetFraction;
    this.#permVerts[index] = this.#pushRadially(this.#permVerts[index], inward, permPush, minRadius);
  }

  #pushRadially(vertex, inwardDir, amount, minRadius) {
    let nx = vertex.x + inwardDir.x * amount;
    let ny = vertex.y + inwardDir.y * amount;
    const radius = Math.hypot(nx, ny);
    if (radius < minRadius) {
      const scale = minRadius / (radius || 1);
      nx *= scale;
      ny *= scale;
    }
    return { x: nx, y: ny };
  }

  #recomputeDamageScalar() {
    let totalDeform = 0;
    for (let i = 0; i < this.#restVerts.length; i++) {
      totalDeform += Math.hypot(
        this.#restVerts[i].x - this.#permVerts[i].x,
        this.#restVerts[i].y - this.#permVerts[i].y
      );
    }
    const avgRadius = this.#restRadii.reduce((a, b) => a + b, 0) / this.#restRadii.length;
    const avgDeform = totalDeform / this.#restVerts.length;
    // damageScalarGain is a pure difficulty tuning knob (see config.js) --
    // it scales the same physical deformation measurement, it doesn't change
    // what's being measured.
    this.#damage = constrain((avgDeform / (avgRadius * 0.6)) * DAMAGE_CONFIG.damageScalarGain, 0, 1);
  }

  // Elastic recovery, then rebuild the actual matter.js collision shape from
  // the live vertex list every frame, so the physics body and the rendered
  // hull are always literally the same data.
  update() {
    // Same mechanism as standard, just a faster spring-back in bouncier
    // profiles (clamped well below 1 so it can never overshoot/oscillate).
    const quickRate = Math.min(DAMAGE_CONFIG.quickRecoveryRate * this.#profile.recoveryRateMultiplier, 0.9);

    for (let i = 0; i < this.#liveVerts.length; i++) {
      const live = this.#liveVerts[i];
      const perm = this.#permVerts[i];
      live.x += (perm.x - live.x) * quickRate;
      live.y += (perm.y - live.y) * quickRate;

      const rest = this.#restVerts[i];
      perm.x += (rest.x - perm.x) * DAMAGE_CONFIG.slowRecoveryRate;
      perm.y += (rest.y - perm.y) * DAMAGE_CONFIG.slowRecoveryRate;
    }
    this.#recomputeDamageScalar();
    this.#applyLiveShapeToBody();
    if (this.#hitFlashFrames > 0) this.#hitFlashFrames--;
  }

  #applyLiveShapeToBody() {
    const body = this.#body;
    const posBefore = { x: body.position.x, y: body.position.y };
    const angleBefore = body.angle;

    const cos = Math.cos(angleBefore);
    const sin = Math.sin(angleBefore);
    const worldVerts = this.#liveVerts.map(v => ({
      x: posBefore.x + (v.x * cos - v.y * sin),
      y: posBefore.y + (v.x * sin + v.y * cos)
    }));

    Body.setVertices(body, worldVerts);

    // Body.setVertices recomputes area/centroid from the new outline and
    // re-anchors the body there internally, which would otherwise make the
    // car visibly "teleport" a fraction of a pixel every time it dents.
    // Since the outline was already built around posBefore/angleBefore, we
    // simply snap both back -- the dent is purely a shape change, not a
    // position change.
    Body.setPosition(body, posBefore);
    Body.setAngle(body, angleBefore);
  }
}

// ===== js/extensions/TyreModel.js =====
/* ============================================================================
   TYRE MODEL (Extension 2, part A -- lateral slip / grip)

   Composed by Car, one instance per body. Runs BEFORE Engine.update() each
   frame (see GameManager.update): matter.js only gives bodies uniform
   frictionAir, which damps speed but does nothing to stop a car sliding
   sideways like a frictionless puck. This decomposes velocity into
   forward/lateral components relative to heading and cancels a fraction of
   the lateral component directly -- a simplified tyre force model matter.js
   does not provide out of the box.
   ============================================================================ */
class TyreModel {
  #grip;
  #slipThreshold;
  #slipRatio = 0;
  #sliding = false;

  constructor(grip, slipThreshold) {
    this.#grip = grip;
    this.#slipThreshold = slipThreshold;
  }

  get slipRatio() { return this.#slipRatio; }
  get isSliding() { return this.#sliding; }

  apply(body, heading) {
    const forward = { x: Math.cos(heading), y: Math.sin(heading) };
    const lateral = { x: -forward.y, y: forward.x };
    const v = body.velocity;

    const forwardComponent = v.x * forward.x + v.y * forward.y;
    const lateralComponent = v.x * lateral.x + v.y * lateral.y;
    const speed = Math.hypot(v.x, v.y);

    this.#slipRatio = speed > 0.05 ? Math.abs(lateralComponent) / speed : 0;
    this.#sliding = this.#slipRatio > this.#slipThreshold;

    // grippedLateral RETAINS `grip` fraction of the lateral component --
    // grip == 1 keeps all of it (no correction, maximum slide); grip == 0
    // removes all of it (full traction, no slide). Counter-intuitively
    // named ("grip" reads as "how gripped", but a HIGH number here means
    // LESS traction) -- documented explicitly since it's easy to reach for
    // the opposite value by instinct. Left as-is rather than flipped: every
    // CAR_TYPES grip constant is tuned against this exact formula.
    const grippedLateral = lateralComponent * this.#grip;
    Body.setVelocity(body, {
      x: forward.x * forwardComponent + lateral.x * grippedLateral,
      y: forward.y * forwardComponent + lateral.y * grippedLateral
    });
  }
}

// ===== js/extensions/SkidLayer.js =====
/* ============================================================================
   SKID LAYER (Extension 2, part B -- persistent skid-mark render target)

   A single off-screen p5.Graphics buffer that accumulates skid marks across
   an entire mode session. This is a render-target technique, not per-frame
   redrawing: marks are stamped once when laid down, and the WHOLE buffer is
   uniformly faded a little every frame via p5's erase() mode, rather than
   every individual mark being tracked/aged/redrawn. That's what lets the
   arena floor accumulate a visible "history" of the fight cheaply.
   ============================================================================ */
class SkidLayer {
  #graphics;

  constructor() {
    this.#graphics = createGraphics(CANVAS_W, CANVAS_H);
    this.#graphics.clear();
  }

  get graphics() { return this.#graphics; }

  stamp(x, y, heading, alpha) {
    const g = this.#graphics;
    g.push();
    g.translate(x, y);
    g.rotate(heading);
    g.noStroke();
    g.fill(15, 15, 18, alpha);
    g.ellipse(0, 0, 6, 2.5);
    g.pop();
  }

  // erase()/noErase() is p5's render-target alpha-erase mode: it reduces the
  // existing pixels' alpha by `strength` wherever the following shape is
  // drawn, instead of painting black over them (which would just tint the
  // floor grey over time). One rect covering the whole buffer each frame
  // fades everything uniformly and cheaply.
  fade() {
    const g = this.#graphics;
    g.erase(SKID_CONFIG.fadeAmount, 255);
    g.noStroke();
    g.rect(0, 0, g.width, g.height);
    g.noErase();
  }

  // Reused (not recreated) across mode switches -- GameManager calls this on
  // setMode() so old marks don't bleed into a new session without allocating
  // a fresh graphics buffer every time (which would leak canvas memory).
  clear() {
    this.#graphics.clear();
  }
}

// ===== js/extensions/RaycastSensor.js =====
/* ============================================================================
   RAYCAST SENSOR (Extension 3, part A -- spatial query for autonomous nav)

   Casts a fan of rays from a car's nose and reports, per ray, the free
   distance before it meets another body. Built on Matter.Query.ray -- a
   genuine spatial query against the live physics world (so it automatically
   respects barriers AND every car's actual current hull, including dented
   ones from DamageModel) rather than a hand-rolled distance/angle check
   against cached positions.
   ============================================================================ */
class RaycastSensor {
  #rayCount;
  #fanAngle;
  #range;
  #lastRays = [];

  constructor(rayCount = RAYCAST_CONFIG.rayCount, fanAngle = RAYCAST_CONFIG.fanAngle, range = RAYCAST_CONFIG.range) {
    this.#rayCount = rayCount;
    this.#fanAngle = fanAngle;
    this.#range = range;
  }

  get lastRays() { return this.#lastRays; }

  sense(car, world) {
    const bodies = Composite.allBodies(world).filter(b => b !== car.body);
    const start = car.position;
    const results = [];

    for (let i = 0; i < this.#rayCount; i++) {
      const t = this.#rayCount === 1 ? 0.5 : i / (this.#rayCount - 1);
      const offset = (t - 0.5) * this.#fanAngle;
      const angle = car.heading + offset;
      const end = {
        x: start.x + Math.cos(angle) * this.#range,
        y: start.y + Math.sin(angle) * this.#range
      };

      const distance = this.#nearestHitDistance(bodies, start, end, angle, car.body);
      results.push({ angleOffset: offset, angle, distance, start, end });
    }

    this.#lastRays = results;
    return results;
  }

  #nearestHitDistance(bodies, start, end, angle, selfBody) {
    // Matter.Query.ray builds a thin rectangular "ray body" internally and
    // runs full SAT collision against each candidate, so collision.supports
    // gives a genuine contact point -- the same field CollisionManager uses
    // for its impact-flash placement, reused here for consistency.
    const collisions = Query.ray(bodies, start, end);
    let nearest = this.#range;

    for (const collision of collisions) {
      const hitBody = collision.bodyB;
      if (!hitBody || hitBody === selfBody) continue;
      const point = (collision.supports && collision.supports[0]) || hitBody.position;
      const d = dist(start.x, start.y, point.x, point.y);
      if (d < nearest) nearest = d;
    }
    return nearest;
  }
}

